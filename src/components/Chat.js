// Encode a string to base64 safely (handles unicode/emoji in tweet text etc.)
const toBase64 = (str) => {
  const bytes = new TextEncoder().encode(str);
  let binary = '';
  for (let i = 0; i < bytes.length; i++) binary += String.fromCharCode(bytes[i]);
  return btoa(binary);
};

const parseCSV = (text) => {
  const lines = text.split('\n').filter((l) => l.trim());
  if (!lines.length) return null;
  const headers = lines[0].split(',').map((h) => h.trim().replace(/^"|"$/g, ''));
  const rowCount = lines.length - 1;

  // Short human-readable preview (header + first 5 rows) for context
  const preview = lines.slice(0, 6).join('\n');

  // Full CSV as base64 â€” avoids ALL string-escaping issues in Python code execution
  // (tweet text with quotes, apostrophes, emojis, etc. all break triple-quoted strings)
  const raw = text.length > 500000 ? text.slice(0, 500000) : text;
  const base64 = toBase64(raw);
  const truncated = text.length > 500000;

  return { headers, rowCount, preview, base64, truncated };
};

// Resize image to stay under Gemini token limits (max 1M tokens)
const MAX_IMAGE_DIM = 512;
const resizeImageBase64 = (data, mimeType) =>
  new Promise((resolve) => {
    const img = new Image();
    img.onload = () => {
      let w = img.width,
        h = img.height;
      if (w <= MAX_IMAGE_DIM && h <= MAX_IMAGE_DIM) return resolve({ data, mimeType });
      if (w > h) {
        h = Math.round((h * MAX_IMAGE_DIM) / w);
        w = MAX_IMAGE_DIM;
      } else {
        w = Math.round((w * MAX_IMAGE_DIM) / h);
        h = MAX_IMAGE_DIM;
      }
      const canvas = document.createElement('canvas');
      canvas.width = w;
      canvas.height = h;
      const ctx = canvas.getContext('2d');
      ctx.drawImage(img, 0, 0, w, h);
      const resized = canvas.toDataURL(mimeType || 'image/jpeg', 0.85);
      resolve({ data: resized.split(',')[1], mimeType: mimeType || 'image/jpeg' });
    };
    img.onerror = () => resolve({ data, mimeType });
    img.src = `data:${mimeType};base64,${data}`;
  });

// Extract plain text from a message (for history only â€” never returns base64)
const messageText = (m) => {
  if (m.parts) return m.parts.filter((p) => p.type === 'text').map((p) => p.text).join('\n');
  return m.content || '';
};

